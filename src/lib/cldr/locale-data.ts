/**
 * Helper function to merge separate locale names files into a single object
 */
function mergeLocaleNames(languages: unknown, territories: unknown, scripts: unknown, variants: unknown) {
  const languagesData = languages as { main: { [key: string]: { localeDisplayNames: { languages: Record<string, string> } } } }
  const territoriesData = territories as { main: { [key: string]: { localeDisplayNames: { territories: Record<string, string> } } } }
  const scriptsData = scripts as { main: { [key: string]: { localeDisplayNames: { scripts: Record<string, string> } } } }
  const variantsData = variants as { main: { [key: string]: { localeDisplayNames: { variants: Record<string, string> } } } }

  const locale = Object.keys(languagesData.main)[0]

  return {
    main: {
      [locale]: {
        localeDisplayNames: {
          languages: languagesData.main[locale].localeDisplayNames.languages,
          territories: territoriesData.main[locale].localeDisplayNames.territories,
          scripts: scriptsData.main[locale].localeDisplayNames.scripts,
          variants: variantsData.main[locale].localeDisplayNames.variants,
        }
      }
    }
  }
}

